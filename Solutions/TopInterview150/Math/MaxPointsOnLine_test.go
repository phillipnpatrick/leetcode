package Solutions

import "testing"

func Test_maxPoints(t *testing.T) {
	type args struct {
		points [][]int
	}
	tests := []struct {
		name string
		args args
		want int
	}{
		{
			// Input: points = [[1,1],[2,2],[3,3]]
			// Output: 3
			name: "test01",
			args: args{
				points: [][]int{{1, 1}, {2, 2}, {3, 3}},
			},
			want: 3,
		},
		{
			// Example 2:
			// Input: points = [[1,1],[3,2],[5,3],[4,1],[2,3],[1,4]]
			// Output: 4
			name: "test02",
			args: args{
				points: [][]int{{1, 1}, {3, 2}, {5, 3}, {4, 1}, {2, 3}, {1, 4}},
			},
			want: 4,
		},
		{
			name: "test03",
			args: args{
				points: [][]int{{0, 0}},
			},
			want: 1,
		},
		{
			name: "test04",
			args: args{
				points: [][]int{{0, 1},{0, 0}},
			},
			want: 2,
		},
		{
			name: "test05",
			args: args{
				points: [][]int{{7,3},{19,19},{-16,3},{13,17},{-18,1},{-18,-17},{13,-3},{3,7},{-11,12},{7,19},{19,-12},{20,-18},{-16,-15},{-10,-15},{-16,-18},{-14,-1},{18,10},{-13,8},{7,-5},{-4,-9},{-11,2},{-9,-9},{-5,-16},{10,14},{-3,4},{1,-20},{2,16},{0,14},{-14,5},{15,-11},{3,11},{11,-10},{-1,-7},{16,7},{1,-11},{-8,-3},{1,-6},{19,7},{3,6},{-1,-2},{7,-3},{-6,-8},{7,1},{-15,12},{-17,9},{19,-9},{1,0},{9,-10},{6,20},{-12,-4},{-16,-17},{14,3},{0,-1},{-18,9},{-15,15},{-3,-15},{-5,20},{15,-14},{9,-17},{10,-14},{-7,-11},{14,9},{1,-1},{15,12},{-5,-1},{-17,-5},{15,-2},{-12,11},{19,-18},{8,7},{-5,-3},{-17,-1},{-18,13},{15,-3},{4,18},{-14,-15},{15,8},{-18,-12},{-15,19},{-9,16},{-9,14},{-12,-14},{-2,-20},{-3,-13},{10,-7},{-2,-10},{9,10},{-1,7},{-17,-6},{-15,20},{5,-17},{6,-6},{-11,-8}},
			},
			want: 6,
		},
		{
			name: "test06",
			args: args{
				points: [][]int{{4,5},{4,-1},{4,0}},
			},
			want: 3,
		},
		{
			name: "test07",
			args: args{
				points: [][]int{{-424,-512},{-4,-47},{0,-23},{-7,-65},{7,138},{0,27},{-5,-90},{-106,-146},{-420,-158},{-7,-128},{0,16},{-6,9},{-34,26},{-9,-166},{-570,-69},{-665,-85},{560,248},{1,-17},{630,277},{1,-7},{-287,-222},{30,250},{5,5},{-475,-53},{950,187},{7,-6},{-700,-274},{3,62},{-318,-390},{7,19},{-285,-21},{-5,4},{53,37},{-5,-1},{-2,-33},{-95,11},{4,1},{8,25},{700,306},{1,24},{-2,-6},{-35,-387},{-630,-245},{-328,-260},{-350,-129},{35,299},{-380,-37},{-9,-9},{210,103},{7,-5},{-3,-52},{-51,23},{-8,-147},{-371,-451},{-1,-14},{-41,6},{-246,-184},{350,161},{-212,-268},{-140,-42},{-9,-4},{-7,5},{10,6},{-15,-191},{-7,-4},{318,342},{-8,-71},{-68,20},{6,119},{6,13},{-280,-100},{140,74},{-760,-101},{0,-24},{-70,-13},{0,2},{0,-9},{106,98}},
			},
			want: 14,
		},
		{
			name: "test08",
			args: args{
				points: [][]int{{1,2},{0,2},{3,2},{5,2}},
			},
			want: 4,
		},
		{
			name: "test09",
			args: args{
				points: [][]int{{-4,-9},{-1,-7},{1,-6},{-16,-17},{14,3},{-7,-11},{-14,-15},{-12,-14}},
			},
			want: 5,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := maxPoints(tt.args.points); got != tt.want {
				t.Errorf("maxPoints() = %v, want %v", got, tt.want)
			}
		})
	}
}
